package com.ij.empbo;

public class empbo {
	public int no;
	public String name;
	public String job;
	public int sal;
	public empbo(int no, String name, String job, int sal) {
		super();
		this.no = no;
		this.name = name;
		this.job = job;
		this.sal = sal;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	
	

}
